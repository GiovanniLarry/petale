async function spinBottle() {
  if (!state.roomId || !state.player || !state.room) {
    return;
  }
  if (spinRequestInFlight) {
    return;
  }
  if (state.room.round?.status === "pending") {
    elements.roomStatus.textContent = "Un tour est deja en cours.";
    return;
  }

  spinRequestInFlight = true;
  elements.spinRoomButton.disabled = true;
  elements.spinButton.disabled = true;
  elements.roomStatus.textContent = "La bouteille tourne...";
  startLocalSpin();

  try {
    const room = await api(`/api/rooms/${state.roomId}/spin`, {
      method: "POST",
      body: { playerId: state.player.id }
    });
    pendingLocalRoom = room;
    window.setTimeout(() => {
      state.room = pendingLocalRoom;
      pendingLocalRoom = null;
      renderLiveRoom(room, { forceReveal: true });
    }, 1700);
  } catch (error) {
    stopLocalSpin();
    elements.roomStatus.textContent = error?.message || "Impossible de lancer le spin.";
  } finally {
    spinRequestInFlight = false;
    updateSpinAvailability(state.room);
  }
}

let isSendingMessage = false; // Prevent double sends

async function sendChatMessage(event) {
  event.preventDefault();
  
  // Prevent multiple simultaneous sends
  if (isSendingMessage) {
    return;
  }
  
  if (!state.roomId || !state.player) {
    return;
  }
  
  isSendingMessage = true;
  
  try {
    // Get all media immediately
    const file = elements.chatFile.files?.[0] || null;
    const text = elements.chatInput.value.trim();
    
    // Check if we have media to send
    const hasMedia = file;
    const hasText = text.length > 0;
    
    // Allow sending media without text
    if (!hasMedia && !hasText) {
      isSendingMessage = false;
      return;
    }
    
    // Store original text for potential restore
    const originalText = text;
    
    // Clear input immediately for better UX
    elements.chatInput.value = "";
    
    let payload = {
      playerId: state.player.id,
      type: "text",
      text: text,
      fileName: hasMedia ? file.name : undefined
    };

    if (hasMedia) {
      try {
        // Process media with optimization for large files
        let media, mediaMime, mediaName;
        
        if (file) {
          // Show processing status for large files
          if (file.size > 5 * 1024 * 1024) {
            elements.roomStatus.textContent = "Traitement du fichier en cours...";
          }
          
          media = await processFileForUpload(file);
          if (!media) {
            // File was too large or processing failed
            elements.chatInput.value = originalText;
            isSendingMessage = false;
            return;
          }
          
          mediaMime = file.type;
          mediaName = file.name;
        }
        
        payload = {
          playerId: state.player.id,
          type: mediaType(mediaMime, mediaName),
          text: text,
          media: media,
          fileName: mediaName
        };
      } catch (error) {
        console.error("Error processing media:", error);
        elements.roomStatus.textContent = "Erreur lors du traitement du média.";
        // Restore input if media processing failed
        elements.chatInput.value = originalText;
        isSendingMessage = false;
        return;
      }
    }

    // Send immediately with timeout for mobile
    const room = await Promise.race([
      api(`/api/rooms/${state.roomId}/messages`, {
        method: "POST",
        body: payload
      }),
      new Promise((_, reject) => setTimeout(() => reject(new Error('Timeout')), 10000))
    ]);
    
    state.room = room;
    clearPendingMedia();
    
    // Render immediately and scroll to bottom
    renderLiveRoom(room);
    
    // Force scroll to bottom immediately after rendering
    setTimeout(() => {
      const chatMessages = document.querySelector('.chat-messages');
      if (chatMessages) {
        chatMessages.scrollTop = chatMessages.scrollHeight;
      }
    }, 50);
    
    // Show success feedback
    if (hasMedia) {
      elements.roomStatus.textContent = "Média envoyé avec succès.";
      setTimeout(() => {
        elements.roomStatus.textContent = "";
      }, 1500);
    }
  } catch (error) {
    console.error("Failed to send message:", error);
    elements.roomStatus.textContent = "Échec de l'envoi. Réessayez.";
    // Restore input if failed
    elements.chatInput.value = originalText;
  } finally {
    // Always reset the sending flag
    isSendingMessage = false;
  }
}

