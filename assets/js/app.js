async function readFileAsDataUrl(file) {
  return new Promise((resolve, reject) => {
    const reader = new FileReader();
    reader.onload = () => resolve(reader.result);
    reader.onerror = reject;
    
    // For large files (videos), use readAsArrayBuffer for better performance
    if (file.size > 10 * 1024 * 1024) { // 10MB threshold
      reader.readAsArrayBuffer(file);
    } else {
      reader.readAsDataURL(file);
    }
  });
}

async function processFileForUpload(file) {
  const maxSize = 50 * 1024 * 1024; // 50MB limit
  const isVideo = file.type.startsWith('video/');
  const isImage = file.type.startsWith('image/');
  
  // For videos, accept larger files but process them efficiently
  if (isVideo && file.size > maxSize) {
    elements.roomStatus.textContent = "Vidéo trop volumineuse. Max 50MB.";
    return null;
  }
  
  // For images, compress if too large
  if (isImage && file.size > 5 * 1024 * 1024) { // 5MB threshold for images
    try {
      const compressedDataUrl = await compressImage(file);
      return compressedDataUrl;
    } catch (error) {
      console.error("Image compression failed:", error);
      // Fall back to original file
    }
  }
  
  // Use optimized file reading
  return await readFileAsDataUrl(file);
}

async function compressImage(file) {
  return new Promise((resolve, reject) => {
    const canvas = document.createElement('canvas');
    const ctx = canvas.getContext('2d');
    const img = new Image();
    
    img.onload = () => {
      // Calculate new dimensions (max 1200px width/height)
      const maxSize = 1200;
      let width = img.width;
      let height = img.height;
      
      if (width > height) {
        if (width > maxSize) {
          height = (height * maxSize) / width;
          width = maxSize;
        }
      } else {
        if (height > maxSize) {
          width = (width * maxSize) / height;
          height = maxSize;
        }
      }
      
      canvas.width = width;
      canvas.height = height;
      
      ctx.drawImage(img, 0, 0, width, height);
      
      // Convert to JPEG with 80% quality for smaller size
      canvas.toBlob((blob) => {
        const reader = new FileReader();
        reader.onload = () => resolve(reader.result);
        reader.onerror = reject;
        reader.readAsDataURL(blob);
      }, 'image/jpeg', 0.8);
    };
    
    img.onerror = reject;
    img.src = URL.createObjectURL(file);
  });
}

function mediaType(mimeType, fileName = "") {
  const safeMime = String(mimeType || "").toLowerCase();
  if (safeMime.startsWith("image/")) {
    return "image";
  }
  if (safeMime.startsWith("video/")) {
    return "video";
  }
  if (safeMime.startsWith("audio/")) {
    return "audio";
  }
  const extension = String(fileName || "").toLowerCase().split(".").pop();
  if (["png", "jpg", "jpeg", "gif", "webp", "bmp", "svg"].includes(extension)) {
    return "image";
  }
  if (["mp4", "webm", "mov", "mkv", "avi", "m4v"].includes(extension)) {
    return "video";
  }
  if (["mp3", "wav", "ogg", "oga", "m4a", "aac", "flac", "opus", "weba"].includes(extension)) {
    return "audio";
  }
  return "text";
}

